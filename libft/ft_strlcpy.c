#include "libft.h"

size_t	ft_strlcpy(char *dest, char *src, size_t size)
{
	size_t			i;
	unsigned int	len_src;

	len_src = ft_strlen(src);
	i = 0;
	if (size != 0)
	{
		while (src[i] != '\0' && i < size - 1)
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (len_src);
}
