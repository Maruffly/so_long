#include "libft.h"

char	*ft_strdup(char *src)
{
	char		*dest;
	size_t		i;
	size_t		n;

	n = ft_strlen(src);
	dest = (char *)malloc((n + 1) * sizeof(char));
	if (dest == NULL)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
